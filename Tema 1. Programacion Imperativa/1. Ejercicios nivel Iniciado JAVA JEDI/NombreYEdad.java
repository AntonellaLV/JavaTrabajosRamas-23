// 8. Mi nombre y edad: Se le solicita que guarde en un espacio de memoria su nombre y apellido,
// luego guarde su edad e imprimalos por pantalla.
// El mensaje debe tener el siguiente formato:

public class NombreYEdad {
    public static void main(String[] args) {
        // Almacenar nombre y apellido
        String nombre = "Antonella";
        String apellido = "Lucero Vetack";

        // Almacenar edad
        int edad = 29;

        // Mostrar por pantalla el nombre y la edad
        System.out.println("Mi nombre es: \"" + nombre + " " + apellido + "\"");
        System.out.println("Mi edad es: " + edad);
    }
}
