// 9. Potencia de 2 y 3: Se le solicita que guarde en un espacio de memoria
// un número y luego haga la potencia de ese número al cuadrado y al cubo.


public class PotenciaDeDosYTres {
    public static void main(String[] args) {
        // Almacenar un número
        int numero = 5; // Cambiar este valor al número que se desee

        // Calcular la potencia al cuadrado y al cubo
        int potenciaCuadrado = numero * numero;
        int potenciaCubo = numero * numero * numero;

        // Mostrar por pantalla los resultados
        System.out.println("El numero es: " + numero);
        System.out.println("La potencia al cuadrado es: " + potenciaCuadrado);
        System.out.println("La potencia al cubo es: " + potenciaCubo);
    }
}
